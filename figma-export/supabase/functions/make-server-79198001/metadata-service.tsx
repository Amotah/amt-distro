import * as kv from './kv_store.tsx';

export interface Contributor {
  id: string;
  name: string;
  role: 'primary_artist' | 'featured_artist' | 'producer' | 'composer' | 'lyricist' | 'remixer';
  ipi?: string; // Interested Party Information number
  isni?: string; // International Standard Name Identifier
}

export interface TrackMetadata {
  id: string;
  releaseId: string;
  title: string;
  version?: string; // e.g., "Remix", "Acoustic", "Live"
  trackNumber: number;
  discNumber: number;
  duration: number; // in seconds
  isrc?: string; // Generated by ISRC service
  isrcRequested?: boolean;
  language: string; // ISO 639-2 code (e.g., 'eng', 'yor', 'hau')
  explicit: boolean;
  genre: string;
  subgenre?: string;
  contributors: Contributor[];
  lyrics?: string;
  audioFilePath: string;
  audioFileUrl: string;
  previewStart?: number; // Preview start time in seconds
  createdAt: string;
  updatedAt: string;
}

export interface ReleaseMetadata {
  id: string;
  userId: string;
  upc?: string; // Generated by UPC service
  upcRequested?: boolean;
  title: string;
  version?: string;
  type: 'single' | 'ep' | 'album';
  artworkPath: string;
  artworkUrl: string;
  primaryArtist: string;
  featuredArtists?: string[];
  label?: string;
  releaseDate: string; // ISO 8601 date
  originalReleaseDate?: string;
  genre: string;
  subgenre?: string;
  copyrightYear: number;
  copyrightText: string; // e.g., "© 2024 Artist Name"
  publishingRights: string; // e.g., "℗ 2024 Label Name"
  language: string;
  trackIds: string[];
  status: 'draft' | 'validated' | 'submitted' | 'live' | 'rejected';
  validationErrors?: string[];
  createdAt: string;
  updatedAt: string;
}

